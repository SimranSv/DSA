/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* middleNode(ListNode* head) {
        ListNode* node=head;
        ll cnt=0;
        while(node!=NULL){
            
            cnt++;
            node=node->next;
        }
        ll kmid=cnt/2;
        ListNode *temp=head;
        if(cnt%2){
            // kmid ++;
            
            while(kmid){
                temp=temp->next;
                kmid--;
                
            }
            
        }else {
             while(kmid){
                temp=temp->next;
                 kmid--;
                
            }
            
        }
        return temp;
        
    }
};